package com.example.logintricyfare;

public class ReportDriver {

    String fname, username, key, email, phonenumber, rdtime, reportDriverName, reportDriverIssue, reportBodyNumber, imgreportdriver;

    public ReportDriver(){

    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    public String getRdtime() {
        return rdtime;
    }

    public void setRdtime(String rdtime) {
        this.rdtime = rdtime;
    }

    public String getReportDriverName() {
        return reportDriverName;
    }

    public void setReportDriverName(String reportDriverName) {
        this.reportDriverName = reportDriverName;
    }

    public String getReportDriverIssue() {
        return reportDriverIssue;
    }

    public void setReportDriverIssue(String reportDriverIssue) {
        this.reportDriverIssue = reportDriverIssue;
    }

    public String getReportBodyNumber() {
        return reportBodyNumber;
    }

    public void setReportBodyNumber(String reportBodyNumber) {
        this.reportBodyNumber = reportBodyNumber;
    }

    public String getImgreportdriver() {
        return imgreportdriver;
    }

    public void setImgreportdriver(String imgreportdriver) {
        this.imgreportdriver = imgreportdriver;
    }
}

